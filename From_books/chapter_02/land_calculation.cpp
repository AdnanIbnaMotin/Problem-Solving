/*
 * One acre of land is equivalent to 43,560 square feet. Write a program that calculates the number
 * of acres in a tract of land with 391,876 square feet.
*/

#include <iostream>

int main(int argc, char* argv[])
{
	std::cout << "Number of acres in a tract of land with 391,876 square feet is: " << 391'876 / 43'560 << std::endl; 
	return 0;
}
